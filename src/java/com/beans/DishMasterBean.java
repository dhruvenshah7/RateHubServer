package com.beans;

public class DishMasterBean {

	private int dishId;
	private String dishName;
	private String deshDesc;
	private int dishCatId;

	public int getDishId() {
		return dishId;
	}

	public void setDishId(int dishId) {
		this.dishId = dishId;
	}

	public String getDishName() {
		return dishName;
	}

	public void setDishName(String dishName) {
		this.dishName = dishName;
	}

	public String getDeshDesc() {
		return deshDesc;
	}

	public void setDeshDesc(String deshDesc) {
		this.deshDesc = deshDesc;
	}

	public int getDishCatId() {
		return dishCatId;
	}

	public void setDishCatId(int dishCatId) {
		this.dishCatId = dishCatId;
	}

}
